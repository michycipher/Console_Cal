let operator = prompt('Input Operator: ');
let num1 = parseInt(prompt('First Number: '));
let num2 = parseInt(prompt('Second Number: '));


if(operator == '+'){
   alert(num1 + num2) 
}else if(operator == '-'){
    alert(num1 - num2)
}else if(operator == '*'){
    alert(num1 * num2)
}else if(operator == '/'){
    alert(num1 / num2)
}else if(operator == '%'){
    alert(num1 % num2)
}